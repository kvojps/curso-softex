class Pessoa:
    def __init__(self, nome, anoNascimento):
        self.nome = nome
        self.anoNascimento = anoNascimento

    def getNome(self):
        return self.nome

    def setNome(self, nome):
        self.nome = nome

    def getAnoNascimento(self, anoNascimento):
        self.anoNascimento = anoNascimento

    def setAnoNascimento(self, anoNascimento):
        self.anoNascimento = anoNascimento