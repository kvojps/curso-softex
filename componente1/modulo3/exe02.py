class NumeroComplexo:
    def getReal(expr):
        return expr.real

    def getImag(expr):
        return expr.imag

    def somaComplex(expr0, expr1):
        return expr0 + expr1
    
    def subtracaoComplex(expr0, expr1):
        return expr0 - expr1

    def multiplicacaoComplex(expr0, expr1):
        return expr0 * expr1

    def divisaoComplex(expr0, expr1):
        return expr0 / expr1